Add-ToolRow $rows "npm" "npm" "frontend and Node package scripts"
Add-ToolRow $rows "pnpm" "pnpm" "frontend package scripts"
Add-ToolRow $rows "Python" "python" "Python services and scripts"
Add-ToolRow $rows "pip" "pip" "Python package management"
Add-ToolRow $rows "Docker" "docker" "local services and containers"
Add-ToolRow $rows "Ollama" "ollama" "local LLM endpoint"
Add-ToolRow $rows "Vivado" "vivado" "FPGA project checks"
Add-ToolRow $rows "Vitis HLS" "vitis_hls" "HLS simulation and synthesis"

$languageServerRows = @(
    "| Java LSP | ``jdtls`` | Java symbol search | $(if (Test-CommandAvailable "jdtls") { "available" } else { "missing" }) | optional; IDEA project model is also acceptable |",
    "| TypeScript LSP | ``typescript-language-server`` | TS/JS symbol search | $(if (Test-CommandAvailable "typescript-language-server") { "available" } else { "missing" }) | optional; editor integration is acceptable |",
    "| Vue LSP | ``vue-language-server`` | Vue SFC symbol search | $(if (Test-CommandAvailable "vue-language-server") { "available" } else { "missing" }) | optional |",
    "| Pyright | ``pyright`` | Python symbol and type checks | $(if (Test-CommandAvailable "pyright") { "available" } else { "missing" }) | optional; Pylance is also acceptable |"
)

$report = @(
    "# Local Toolchain Detection",
    "",
    "Generated by ``scripts/detect-local-toolchain.ps1``.",
    "",
    "This report is advisory. Do not install missing tools or start services without user confirmation.",
    "",
    "## Core Tools",
    "",
    "| Tool | Command | Purpose | Status | Version |",
    "| --- | --- | --- | --- | --- |"
) + $rows + @(
    "",
    "## Language / Symbol Tools",
    "",
    "| Tool | Command | Purpose | Status | Notes |",
    "| --- | --- | --- | --- | --- |"
) + $languageServerRows + @(
    "",
    "## Next Steps",
    "",
    "- Copy relevant findings into the local toolchain check document under docs.",
    "- If a required tool is missing, record it as a risk or setup task first.",
    "- Prefer project-local validation commands from ``.codex/commands.md`` over global guesses."
)

if ([string]::IsNullOrWhiteSpace($OutputPath)) {
    $report
} else {
    $parent = Split-Path -Parent $OutputPath
    if (-not [string]::IsNullOrWhiteSpace($parent) -and -not (Test-Path -LiteralPath $parent)) {
        New-Item -ItemType Directory -Force -Path $parent | Out-Null
    }
    Set-Content -LiteralPath $OutputPath -Value $report -Encoding UTF8
    Write-Host "[ok] wrote $OutputPath"
}
