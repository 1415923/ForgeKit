param(
    [string]$OutputPath = ""
)

$ErrorActionPreference = "Stop"

function Test-CommandAvailable {
    param([string]$Name)
    return [bool](Get-Command $Name -ErrorAction SilentlyContinue)
}

function Get-CommandVersion {
    param(
        [string]$Name,
        [string[]]$VersionArguments = @("--version")
    )

    if (-not (Test-CommandAvailable $Name)) {
        return "missing"
    }

    try {
        $output = & $Name @VersionArguments 2>&1 | Select-Object -First 1
        if ([string]::IsNullOrWhiteSpace($output)) {
            return "available"
        }
        return ($output.ToString()).Trim()
    } catch {
        return "available, version check failed"
    }
}

function Add-ToolRow {
    param(
        [System.Collections.Generic.List[string]]$Rows,
        [string]$Tool,
        [string]$Command,
        [string]$Purpose,
        [string[]]$VersionArgs = @("--version")
    )

    $status = if (Test-CommandAvailable $Command) { "available" } else { "missing" }
    $version = Get-CommandVersion -Name $Command -VersionArguments $VersionArgs
    $Rows.Add("| $Tool | ``$Command`` | $Purpose | $status | $version |") | Out-Null
}

$rows = New-Object System.Collections.Generic.List[string]
Add-ToolRow $rows "Git" "git" "version control"
